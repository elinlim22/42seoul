#include "libft.h"

int	ft_atoi(const char *str)
{
	long long	n;
	int			i;

	i = 1;
	n = 0;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			i *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		n *= 10;
		n += *str - '0';
		str++;
	}
	n *= i;
	if (n < -2147483648 || n > 2147483647)
		exit(1);
	return ((int)n);
}
